function nonSpecifiedFormatter(num, digits)
    digits = digits or 1
    
    local lookup = {
        { value = 1, symbol = '' },
        { value = 1e3, symbol = 'K' },
        { value = 1e6, symbol = 'M' },
        { value = 1e9, symbol = 'B' },
        { value = 1e12, symbol = 'T' },
        { value = 1e15, symbol = 'P' },
        { value = 1e18, symbol = 'E' }
    }
    
    local function round(num, numDecimalPlaces)
        local mult = 10^(numDecimalPlaces or 0)
        return math.floor(num * mult + 0.5) / mult
    end
    
    local function formatNumber(n, precision)
        local factor = 10^precision
        return tostring(round(n * factor) / factor)
    end

    local function replacePattern(s, pattern, replacement)
        return s:gsub(pattern, replacement)
    end

    for i = #lookup, 1, -1 do
        local item = lookup[i]
        if num >= item.value then
            local formattedNumber = formatNumber(num / item.value, digits)
            local cleanedNumber = replacePattern(formattedNumber, "%.0+$", "")
            cleanedNumber = replacePattern(cleanedNumber, "(%.[0-9]*[1-9])0+$", "%1")
            return cleanedNumber .. item.symbol
        end
    end
    return "0"
end
