function enum(t)
    local data = {}
    local reverse = {}

    for id, name in ipairs(t) do
        data[name] = id
        reverse[id] = name
    end

    return setmetatable({}, {
        __data = data,
        __reverse = reverse,
        __index = function(self, key)
            local mt = getmetatable(self)
            local value = mt.__data[key] or mt.__reverse[key]
            assert(value, 'not found: ' .. key)
            return value
        end
    })
end

function split(str, delimiter)
    local parts = {}
    local pattern = string.format("([^%s]+)", delimiter)
    str:gsub(pattern, function(part) parts[#parts + 1] = part end)
    return parts
end

function GetTattooTypeDictionaryAndName(t)
    assert(type(t) == 'string', 't must be a string')
    local delimiter = '__'
    local index = t:find(delimiter)
    if index then
        local dictionary = t:sub(1, index - 1)
        local name = t:sub(index + #delimiter)
        return dictionary, name
    else
        return t, ''
    end
end

function FormatTattoo(Name,Part)
    return Part.."__"..Name
end

