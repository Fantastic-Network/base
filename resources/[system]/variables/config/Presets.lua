exports("GetLocationPresets",function(Group)
    local Ped = PlayerPedId()
    local Coords = GetEntityCoords(Ped)
    for _,v in pairs(LocationPresets) do
        local Distance = #(Coords - vec3(v[1],v[2],v[3]))
        if Distance <= v[5] then
            if Group == v[4] then
                return true
            end
        end
    end
end)